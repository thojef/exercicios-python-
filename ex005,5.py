# USANDO OPERADORES ARITIMÉTICOS

n1 = float (input('digite um valor: '))
n2 = float (input('outro valor: '))

s = n1 + n2
sb = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
rd = n1 % n2

print (f'A soma resulta em: {s} \n'
       f'A subtraçao resulta em: {sb} \n'
       f'A multiplicaçao resulta em: {m} \n'
       f'A divisao resulta em: {d} \n'
       f'A divisao inteira resulta em {di} \n'
       f'O resto de divisao resulta em: {rd} \n'
       f'A exponenciaçao resulta em: {e}')


